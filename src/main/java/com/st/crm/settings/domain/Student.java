package com.st.crm.settings.domain;

import java.util.Objects;

/**
 * 学生类
 * @author Grimes
 */
public class Student {
    private String id;
    private String name;
    private int age;
    private String telPhone;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getTelPhone() {
        return telPhone;
    }

    public void setTelPhone(String telPhone) {
        this.telPhone = telPhone;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Student student = (Student) o;
        return age == student.age && Objects.equals(id, student.id) && Objects.equals(name, student.name) && Objects.equals(telPhone, student.telPhone);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, name, age, telPhone);
    }

    @Override
    public String toString() {
        return "Student{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", age=" + age +
                ", telPhone='" + telPhone + '\'' +
                '}';
    }
}
